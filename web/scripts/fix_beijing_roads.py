import hashlib
for b in bj:
    region = b.get('region','')
    h = int(hashlib.md5(b['name'].encode()).hexdigest(),16)
    roads = list(ROAD_INDUSTRIES.get(region, {}).items())
    if roads:
        road_name, (industry, desc) = roads[h % len(roads)]
        b['road'] = road_name        # 精准到路
        b['road_desc'] = desc        # 该路产业聚集描述
        # 用路段产业覆盖原industry_tags
        b['industry_tags'] = [industry]
    else:
        b['road'] = region
        b['road_desc'] = ''

json.dump(bj, open('public/data/buildings.json','w'), ensure_ascii=False, indent=2)
