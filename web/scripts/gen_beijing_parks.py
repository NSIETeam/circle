added = 0
for name,park,region,industry,coord in parks:
    if name in existing_names: continue
    lat,lng = coord
    h = int(hashlib.md5(name.encode()).hexdigest(),16)
    area = 1500 + (h % 40)*500
    fh = 4 + (h % 5)*2
    fl = 1 + (h % 6)
    pc = 1000 + (h % 30)*1000
    rmin = round(0.6 + (h % 20)*0.1,1)
    rmax = round(rmin + 0.5 + (h % 10)*0.1,1)
    months = 1 + (h % 3)
    comm = round(rmin*area*30*months/10000,1)
    b = {
        'id': 'bj_%05d' % (h%99999),
        'name': name, 'park_name': park, 'region': region, 'city':'北京',
        'total_area': area, 'floor_height': fh, 'floor_load': fl, 'power_capacity': pc,
        'rent_min': rmin, 'rent_max': rmax, 'industry_tags':[industry],
        'images':[img_pool[h%6]], 'is_featured': (h%4==0),
        'park_rating': round(3.5 + (h%15)*0.1,1), 'tenant_count': 50 + (h%200),
        'amenities':[facilities[(h+i)%len(facilities)] for i in range(6)],
        'latitude': lat, 'longitude': lng,
        'commission': comm, 'commission_rate': '%d个月租金' % months,
        'policy':[policies[(h+i)%len(policies)] for i in range(3)],
        'surrounding':['地铁2公里内','高速出口5分钟','员工宿舍','食堂'][:2+(h%3)],
        'main_type': types[h%len(types)],
    }
    data.append(b)
    added += 1

json.dump(data, open('public/data/buildings.json','w'), ensure_ascii=False, indent=2)
