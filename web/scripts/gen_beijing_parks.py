img_pool = ['/images/buildings/industrial%d.jpg' % i for i in range(1,7)]
policies = ['租金补贴最高50万','税收奖励区级留存50%','高新企业申报辅导','专精特新奖励20万','人才落户绿色通道','研发补贴最高100万','产业基金优先投资','行政审批协助']
