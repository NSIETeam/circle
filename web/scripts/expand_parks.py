added = 0
for name, road, region, industry, coord, main_type in new_parks:
    if name in existing: continue
    lat, lng = coord
    h = int(hashlib.md5(name.encode()).hexdigest(),16)
    area = 1000 + (h % 60)*500
    fh = 3 + (h % 6)*2
    fl = 1 + (h % 6)
    pc = 800 + (h % 40)*1000
    rmin = round(0.5 + (h % 25)*0.1,1)
    rmax = round(rmin + 0.5 + (h % 10)*0.1,1)
    months = 1 + (h % 3)
    comm = round(rmin*area*30*months/10000,1)
    b = {
        'id': 'bj2_%05d' % (h%99999),
        'name': name, 'park_name': name, 'region': region, 'city':'北京',
        'total_area': area, 'floor_height': fh, 'floor_load': fl, 'power_capacity': pc,
        'rent_min': rmin, 'rent_max': rmax, 'industry_tags':[industry],
        'images':[img_pool[h%6]], 'is_featured': (h%3==0),
        'park_rating': round(3.5 + (h%15)*0.1,1), 'tenant_count': 30 + (h%250),
        'amenities':[facilities[(h+i)%len(facilities)] for i in range(6)],
        'latitude': lat, 'longitude': lng,
        'commission': comm, 'commission_rate': '%d个月租金' % months,
        'policy':[policies[(h+i)%len(policies)] for i in range(3)],
        'surrounding':[surroundings[(h+i)%len(surroundings)] for i in range(4)],
        'main_type': main_type,
        'road': road,
        'road_desc': '%s产业聚集区' % industry,
    }
    data.append(b)
    added += 1

json.dump(data, open('public/data/buildings.json','w'), ensure_ascii=False, indent=2)
from collections import Counter
types = Counter(b.get('main_type','') for b in data)
print('新增%d，总计%d' % (added, len(data)))
